android {
    namespace = "com.opensiri.agent.bootstrap"
    compileSdk = 35

    defaultConfig {
        applicationId = "com.opensiri.agent.bootstrap"
        minSdk = 26
        // targetSdk 28 allows executing binaries from app data directory.
        // Android 10+ (targetSdk 29+) enforces W^X via SELinux which blocks
        // proot from executing Termux binaries. Termux (F-Droid) uses the same approach.
        // Note: Google Play rejects targetSdk < 34 — side-load only.
        targetSdk = 28
        versionCode = 2
        versionName = "0.2.0"
    }

    // ── Product flavors: bootstrap vs complete/offline ────────────────────
    // Bootstrap:  ~39 MB APK, downloads Termux deps on first launch
    // Complete:   ~225 MB APK, pre-bundles all deps (no internet needed)
    flavorDimensions += "edition"
    productFlavors {
        create("bootstrap") {
            dimension = "edition"
            applicationIdSuffix = ".bootstrap"
            versionNameSuffix = "-bootstrap"
        }
        create("complete") {
            dimension = "edition"
            applicationIdSuffix = ".complete"
            versionNameSuffix = "-complete"
        }
    }

    buildTypes {
        release {
            isMinifyEnabled = false
        }
    }

    compileOptions {
        sourceCompatibility = JavaVersion.VERSION_17
        targetCompatibility = JavaVersion.VERSION_17
        isCoreLibraryDesugaringEnabled = true
    }

    kotlinOptions {
        jvmTarget = "17"
    }

    buildFeatures {
        compose = true
    }

    packaging {
        resources {
            excludes += "/META-INF/{AL2.0,LGPL2.1}"
        }
    }

    // Don't compress bootstrap zip, .deb packages, or .tar.gz bundles
    androidResources {
        noCompress += listOf("zip", "tar.gz", "deb")
    }

    // ── Flavor-specific asset configuration ───────────────────────────────
    // The complete flavor includes pre-bundled .deb packages (in assets/complete/)
    // generated by scripts/prebuild-complete.sh before the Gradle build.
    // The bootstrap flavor does NOT include these — it downloads deps on first launch.
    sourceSets {
        getByName("complete") {
            assets.srcDirs("src/main/assets", "src/complete/assets")
        }
    }
}

